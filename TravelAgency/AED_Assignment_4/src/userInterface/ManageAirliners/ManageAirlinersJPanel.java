    public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)tblManageAirliner.getModel();
        dtm.setRowCount(0);
        
        
        for(Airliner ai:aCatalog.getAirliner())
        {
            Object[] row = new Object[2];
            row[0]=ai;
            row[1]=ai.getAirlinerName();
            
            dtm.addRow(row);
        
        
        }
    
    }
    
    
    
