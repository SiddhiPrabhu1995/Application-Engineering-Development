    private void btnViewAirlineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewAirlineActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManageAirliner.getSelectedRow();
        
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null,"Please select a row from the data first","Warning!",JOptionPane.WARNING_MESSAGE);
          
        }
        else{
            Airliner airliner =(Airliner) tblManageAirliner.getValueAt(selectedRow,0);
            ViewAirlinerJPanel panel = new ViewAirlinerJPanel(userControlDisplayPanel,airliner);
            userControlDisplayPanel.add("ViewAirline",panel);
            CardLayout layout = (CardLayout)userControlDisplayPanel.getLayout();
            layout.next(userControlDisplayPanel);
        
        }
        
    }//GEN-LAST:event_btnViewAirlineActionPerformed

